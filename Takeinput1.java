
import java.util.*;
public class Takeinput1 {

	public static void main(String[] args) {
	Scanner sc = new Scanner (System.in);
	String remo = sc.next();   //------->//this is for insertion of input int type
	System.out.println(remo);   
	
	String name = sc.nextLine();   //-------->//this is for insertion of input string type
	System.out.println(name);
	
	int input = sc.nextInt();  //--------->//this is for insertion of taking input as integer.
	System.out.println(input);
	
	float price = sc.nextFloat();//------>//this is for taking insertion of taking input as float.
	System.out.println(price);
	}

}
